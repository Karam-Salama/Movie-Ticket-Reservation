    private void getReservedSeats() {
        //get the selected time
        String time = (String) movieTime.getSelectedItem();
        ArrayList<String> reservedSeats = reservationFile.getReservedSeats(movieName, time);
        // Update icons for reserved seats
        for (String seat : reservedSeats) {
            JButton seatButton = findButtonByName(seat);
            if (seatButton != null) {
                updateSeatIcon(seatButton, "reserved");
            }
        }
    }

    private JButton findButtonByName(String seatName) {
        // Retrieve the button directly from the map
        return seatButtonMap.get(seatName);
    }

    private void makeAllSeatsAvailable() {
        // Iterate through each entry in the seatButtonMap
        for (Map.Entry<String, JButton> entry : seatButtonMap.entrySet()) {
            JButton seatButton = entry.getValue();
            updateSeatIcon(seatButton, "available"); // Set each button to "available"
        }
    }

    private void seatButtonClicked(JButton seatButton, String buttonName) {
        if (seatButton.getIcon().toString().contains("reservedSeat.png")) {
            return;
        }
        int maxSeats = getMaxSeats(); // Get the maximum number of seats allowed based on the selected radio button

        if (!selectedSeats.contains(buttonName)) {
            if (selectedSeats.size() < maxSeats) {
                selectedSeats.add(buttonName);
                updateSeatIcon(seatButton, "selected");
                System.out.println(selectedSeats);
            } else {
                
                System.out.println("Maximum number of seats selected!");
            }
        } else {
            selectedSeats.remove(buttonName);
            updateSeatIcon(seatButton, "available");
            System.out.println("after remove" + selectedSeats);
        }
    }

    private void removeSelected() {
        selectedSeats.clear();
    }

    private int getMaxSeats() {
        if (NoOfSeats1.isSelected()) {
            return 1;
        } else if (NoOfSeats2.isSelected()) {
            return 2;
        } else if (NoOfSeats3.isSelected()) {
            return 3;
        }
        return 1; // Default to 1 if no radio button is selected
    }

    private void updateSeatIcon(JButton seatButton, String state) {
        // updates the icon of the seatButton based on its state (selected, available, or unavailable)
        switch (state) {
            case "selected":
                seatButton.setIcon(new ImageIcon("src\\seats\\selectedSeat.png"));
                break;
            case "available":
                seatButton.setIcon(new ImageIcon("src\\seats\\availableSeat.png"));
                break;
            case "reserved":
                seatButton.setIcon(new ImageIcon("src\\seats\\reservedSeat.png"));
                break;
            default:
                seatButton.setIcon(new ImageIcon("src\\seats\\availableSeat.png"));
        }
    }
