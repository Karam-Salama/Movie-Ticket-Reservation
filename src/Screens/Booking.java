    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed

        String customername = ReservName.getText();
        String showtime = (String) movieTime.getSelectedItem();
        if (customername.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter reservation name!");
        } else if (selectedSeats.size() != getMaxSeats()) {
            JOptionPane.showMessageDialog(this, "Please select the desired seat!");
        } else {
            Reservation res = new Reservation(movieName, showtime, customername, selectedSeats);
            if (res.processBooking()) {
                ReservName.setText("");
                movieTime.setSelectedIndex(0);
                NoOfSeats1.setSelected(true);

                confirmationScreen confirm = new confirmationScreen(selectedSeats);
                confirm.setVisible(true);
                confirm.setLocationRelativeTo(null);
                //after confirming make the selected seats as reserved
                getReservedSeats();
            }
        }
    }//GEN-LAST:event_bookBtnActionPerformed

